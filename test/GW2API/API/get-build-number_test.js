var GW2API = require('../../../GW2API');

var expect = require('chai').expect;

let functionCall = GW2API.API.getBuildNumber;

describe('testing getBuildNumber', () => {
    it('should return an object with property id', async () => {
        var result = await functionCall();
        expect(result.data).to.have.property('id');
    });
    
    it('should return an object with property id that is a number', async () => {
        var result = await functionCall();
        expect(result.data).to.have.property('id').that.is.a('number');
    });
});